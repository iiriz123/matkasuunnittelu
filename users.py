from werkzeug.security import generate_password_hash, check_password_hash

import db

def get_user(user_id):
    sql= "SELECT id, username FROM users WHERE id = ?"
    result = db.query(sql, [user_id])
    return result[0] if result else None

def item_count(user_id):
    sql = """SELECT COUNT(id) FROM items
            WHERE items.user_id = ?"""
    return db.query(sql, [user_id])[0][0]

def get_items(user_id, page, page_size):
    sql= """SELECT items.id, items.destination, items.start_date, items.end_date,
                   COUNT(comments.id) AS comment_count
             FROM items 
             LEFT JOIN comments ON items.id = comments.item_id
             WHERE items.user_id = ?
             GROUP BY items.id
             ORDER BY items.id DESC
             LIMIT ? OFFSET ?"""
    limit = page_size
    offset = page_size * (page - 1)
    return db.query(sql, [user_id, limit, offset])

def create_user(username, password):
    password_hash = generate_password_hash(password)
    sql = "INSERT INTO users (username, password_hash) VALUES (?, ?)"
    db.execute(sql, [username, password_hash])

def check_login(username, password):
    sql = "SELECT id, password_hash FROM users WHERE username = ?"
    result = db.query(sql, [username])
    if not result:
        return None
    user_id = result[0]["id"]
    password_hash = result[0]["password_hash"]
    if check_password_hash(password_hash, password):
        return user_id
    else:
        return None
